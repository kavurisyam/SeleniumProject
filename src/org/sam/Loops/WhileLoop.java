/* 1) The Java while loop is used to iterate a part of the program repeatedly until the specified Boolean condition 
    is true.
   2) As soon as the Boolean condition becomes false, the loop automatically stops. 
    The while loop is considered as a repeating if statement*/

package org.sam.Loops;

public class WhileLoop {

	public static void main(String[] args) {
     int i=0;
     while (i<50) {
    	 i=i+1;
    	 System.out.println(i);
    
    	 
     }
	}

}
