/* 1) The “for” loop in Java is an entry-controlled loop that facilitates a user to execute a block of a statement(s) 
      iteratively for a fixed number of times.
   2) The number of iterations depends on the test-condition given inside the “for” loop. 
    The Java “for” loop is one of the easiest to understand Java loops.*/
package org.sam.Loops;

public class ForLoop {
		public static void main(String[] args) {
			int i;
			for(i=0;i<5;i++) {
				System.out.println(i);
	
			}
		}

}

