/* In Java, the if...else statement is a control flow statement that allows 
 you to execute a block of code only if a certain condition is met.*/

package org.sam.ConditionalStatements;

public class Ifelse {
	public static void main(String args[]){
		int x=20;
		int y=30;
		if(x>y) {
		System.out.println("X is greater than Y");
		}
		else {
		System.out.println("Y is greater than X");	
		}
		}
}
